    private void selecionarPoltrona(java.awt.event.MouseEvent evt) {
        JPanel assento = (JPanel) evt.getComponent();
        final Color statusAssento = assento.getBackground();
        Integer nrAssento = Integer.valueOf(assento.getName());
        if (statusAssento == Color.RED) {
            return;
        }
        if (statusAssento == Color.YELLOW) {
            assento.setBackground(Color.GREEN);
            assentosSelecionados.remove(nrAssento);
            return;
        }
        assento.setBackground(Color.YELLOW);

        Passagem passagem = new Passagem();
        passagem.setNumeroAssento(nrAssento);
        passagem.setViagem(viagem);
        passagem.setNomePassageiro(jTextPane1.getText());
        passagem.setUsuario(TelaInicial.usuario);
        passagem.setDocumentoPassageiro(jTextPane2.getText());
        passagem.setFormaPgto("DINHEIRO");
        assentosSelecionados.put(nrAssento, passagem);
    }

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        StringBuilder sb = new StringBuilder();
        sb.append("---------Confirmação---------\n");
        if (assentosSelecionados.size() == 0) {
            JOptionPane.showMessageDialog(this, "Selecione pelo menos um assento");
            return;
        }
        BigDecimal soma = BigDecimal.ZERO;
        for (Integer i : assentosSelecionados.keySet()) {
            Passagem p = assentosSelecionados.get(i);
            sb.append("Assento: " + p.getNumeroAssento() + " - Valor: R$ " + p.getViagem().getValor() + "\n");
            soma = soma.add(p.getViagem().getValor());
        }
        sb.append("Total: R$ " + soma + "\n\nConfirmar Compra?");
        int resultado = JOptionPane.showConfirmDialog(this, sb.toString(), "Confirmacão", JOptionPane.YES_NO_OPTION);
        if (resultado == JOptionPane.YES_OPTION) {
            for (Integer i : assentosSelecionados.keySet()) {
                Passagem p = assentosSelecionados.get(i);
                passagemFacadeRemote.create(p);
            }
            JOptionPane.showMessageDialog(this, "Compra confirmada");
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

