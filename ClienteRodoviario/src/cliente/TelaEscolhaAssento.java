    private void carregarInformacoesTela() {
        DateFormat dataFormato = new SimpleDateFormat("dd/MM/yyyy");
        DateFormat horaFormato = new SimpleDateFormat("HH:mm");
        lblLinha.setText(viagem.getLinha().getNome());
        lblData.setText(dataFormato.format(viagem.getData()));
        lblHorario.setText(horaFormato.format(viagem.getData()));
        lblOrigem.setText(viagem.getOrigem().getNome() + "/" + viagem.getOrigem().getEstado());
        lblDestino.setText(viagem.getDestino().getNome() + "/" + viagem.getDestino().getEstado());
        lbValor.setText("R$ " + viagem.getValor());
    }
